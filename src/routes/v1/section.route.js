const express = require('express');
const validate = require('../../middlewares/validate');
const sectionController = require('../../controllers/section.controller');
const sectionValidation = require('../../validations/section.validation');

const router = express.Router();

router
  .route('/')
  .post(validate(sectionValidation.createSection), sectionController.createSection)
  .get(validate(sectionValidation.querySection), sectionController.querySection);

router
  .route('/:sectionId')
  .get(validate(sectionValidation.getSection), sectionController.getSection)
  .patch(validate(sectionValidation.updateSection), sectionController.updateSection)
  .delete(validate(sectionValidation.deleteSection), sectionController.deleteSection);

module.exports = router;

